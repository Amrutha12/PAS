    private void btndwnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndwnActionPerformed
        // TODO add your handling code here:
        Connection con=null;
        int att=attachfile();
        if(att==0)
        {
            JOptionPane.showMessageDialog(this,"No attachment found!!");
        }
        else
        {
         String mypath="";
        int reval=fileattach.showSaveDialog(this);
        if(reval==javax.swing.JFileChooser.APPROVE_OPTION)
        {
            mypath=fileattach.getCurrentDirectory().toString();
            generalclass ob=new generalclass();
            byte[] fileBytes;
            String query;
            con=ob.getconnect();
            try
            {
               query="select attach from message_table where ID='"+value+"'";
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery(query);
                if(rs.next())
                {
                    fileBytes=rs.getBytes(1);
                    OutputStream targetfile=new FileOutputStream(mypath+"//"+"uuu");
                    targetfile.write(fileBytes);
                    targetfile.close();
                }
                JOptionPane.showMessageDialog(this,"saved");
            }
            catch(Exception ex)
            {
                
            }
        }
      
        }
    
    }//GEN-LAST:event_btndwnActionPerformed

