    private void btnsendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsendActionPerformed
        // TODO add your handling code here:
        try
        {
//            string date=date
            File file=new File(path.getSelectedFile().toString());
            FileInputStream fi=new FileInputStream(file);
            int len=(int) file.length();
            Connection con=null;
            con=ob.getconnect();
            PreparedStatement ps;
            String query="insert into message_table(sender,receiver,status,subject,message,attach,date)values(?,?,?,?,?,?,getdate())";
            ps=con.prepareStatement(query);
            ps.setString(1,unam);
            ps.setString(2,txtto.getText());
            ps.setString(3,"sent");
            ps.setString(4, txtsub.getText());
            ps.setString(5,txtmessage.getText());
            ps.setBinaryStream(6,fi,len);
            ps.executeUpdate();
            lblstatus.setText("Your message has been send....");
        }
        catch(Exception ex)
        {
         System.out.print(ex.toString());
        }
    }//GEN-LAST:event_btnsendActionPerformed

